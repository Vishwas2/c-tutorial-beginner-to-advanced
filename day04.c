/* What is a variable?
-> A name given to a memory location
-> Declared by typing type variable_name;           eg: int a,b;
-> Initialized and declared by type variable_name=value;        eg: int a=4; */

/*RULES FOR DEFINING A VARIABLE IN C:
-> Can contain alphabets,digits and underscore
-> A variable name can start with alphabet or underscore only
-> Can't start with a digit
-> No whitespaces & reserved keywords are allowed
-> Valid variable names: int vishwas, float vishwas123, char _vishwas25
-> Invalid variable names: $vishwas, int 55vishwas, char long;*/

/*
-> Basic Data Types: int,char,float,double
-> Derived Data Types: array,pointers,structure,union
-> Enumeration Data Types: emum
-> Void Data Type: void*/

